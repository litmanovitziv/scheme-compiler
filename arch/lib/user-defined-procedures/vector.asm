
VECTOR_BODY:
	PUSH(FP);
	MOV(FP,SP);

	PUSH(R1);
	PUSH(R2);
	MOV(R1,FPARG(1));
	MOV(R2,R1);
	MOV(R3,1);
	ADD(R1,2);
VEC_LOOP:
	CMP(R3,R1);
	JUMP_EQ(END_VEC);
	PUSH(FPARG(R3));
	INCR(R3);
	JUMP(VEC_LOOP);
END_VEC:
	PUSH(R2);
	CALL(MAKE_SOB_VECTOR);
	POP(R2);
	POP(R1);
	MOV(SP,FP);
	POP(FP);
	RETURN;